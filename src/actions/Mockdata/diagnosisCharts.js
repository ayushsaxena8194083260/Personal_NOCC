const getSeries = (coll) => {
   let series = [];
   const labels = getUniqueValues(Object.values(coll.map((item) => { return item.individual_name0 })));
   series = labels.map((item, index) => {
      return {
         marker : {symbol : 'square',radius : 12 },
         name: item,
         data: Object.values(coll.filter((flt) => flt.individual_name0 == item).map((col) => { return parseFloat(col.data0) }))
      }
   })

   return series;
}

const getUniqueValues = (items) => {
   const uniqueItem = [];
   items.map(item => {
      if (uniqueItem.indexOf(item) === -1) {
         uniqueItem.push(item)
      }
   });

   return uniqueItem;
}